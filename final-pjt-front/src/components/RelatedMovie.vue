<template>
  <div class="movie_container" style="cursor:pointer" @click="getHistory">
      <a class="item">
      <img :src="imgSrc" alt="" id="img" onerror="this.style.display='none'">
      </a>
  </div>
</template>

<script>
import axios from 'axios'
const API_URL = "http://127.0.0.1:8000"

export default {
  name: 'RelatedMovieVue',
  props: {
    movie: Object,
  },
  computed: {
    imgSrc() {
        const Url = `https://www.themoviedb.org/t/p/w500/${this.movie.poster_path}`
        // console.log(this.movie)
        return Url
    }
  },
  methods: {
    getHistory() {
      if (this.$store.state.token === null) {
        alert('로그인이 필요한 서비스입니다')
        this.$router.push({ name: 'LogInView'})
      } else {
      axios({
        method: 'post',
        url:`${API_URL}/api/v1/movies/history/${this.movie?.movie_id}/`,
        headers: { 
            Authorization: `Token ${this.$store.state.token}`
        }
      })
      .then(() => {
        // console.log('이동!')
        this.$router.push({ name : 'MovieDetailView', params: {movieId: this.movie.id} }).catch(()=>{})
      })
      .catch((err) => {
        console.log(err)
      })
    }
    },
  }
}
</script>

